// js is a dynamic type programming language

// primitive data types
const a = 5;
const b = "have a good day!";
const c = true;
console.log(typeof a, typeof b, typeof c);

// non primitive data types
const d = { id: 1, name: "sujat" };
const e = [45, 43, 56, 20, 5];
console.log(typeof d, Array.isArray(e));

// special data types
let f;
console.log(typeof f);
const g = null;
console.log(typeof g); //null type is denoted by object type
